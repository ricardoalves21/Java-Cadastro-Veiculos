    private void exibeInformacoes() {
        ArrayList<Veiculo> veiculos = cv.retornarTodos();
        
        // para limpar o JTable
        for (int i=0; i < 500; i++) {
            tblVeiculos.setValueAt("", i, 0); // para setar um valor nas coordenadas da tabela (linha i e coluna 0)
            tblVeiculos.setValueAt("", i, 1); // para setar um valor nas coordenadas da tabela (linha i e coluna 1)
        }
        
        // para preencher o JTable
        for (int i = 0; i < veiculos.size() && veiculos.get(i) != null; i++) {
            tblVeiculos.setValueAt(veiculos.get(i).getNome(), i, 0);
            tblVeiculos.setValueAt(veiculos.get(i).getNome(), i, 1);
        }
    }

        
        
